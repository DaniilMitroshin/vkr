def md_escape_text(text: str) -> str:
    return text.strip()


def html_paragraph(text: str) -> str:
    text = text.strip()
    if text.startswith("<"):
        return text
    return f"<p>{escape(text)}</p>"


def xml_escape(text: str) -> str:
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def strip_tags(text: str) -> str:
    text = re.sub(r"<[^>]+>", " ", text)
    return re.sub(r"\s+", " ", text).strip()


def w_run(text: str, bold: bool = False, italic: bool = False, size: int = 28) -> str:
    props = [
        '<w:rFonts w:ascii="Times New Roman" w:hAnsi="Times New Roman" w:cs="Times New Roman"/>',
        f'<w:sz w:val="{size}"/>',
        f'<w:szCs w:val="{size}"/>',
    ]
    if bold:
        props.append("<w:b/>")
        props.append("<w:bCs/>")
    if italic:
        props.append("<w:i/>")
        props.append("<w:iCs/>")
    return f"<w:r><w:rPr>{''.join(props)}</w:rPr><w:t xml:space=\"preserve\">{xml_escape(text)}</w:t></w:r>"


def w_paragraph(
    text: str,
    *,
    align: str = "both",
    indent: bool = True,
    bold: bool = False,
    italic: bool = False,
    size: int = 28,
    page_break_before: bool = False,
) -> str:
    ppr = [
        f'<w:jc w:val="{align}"/>',
        '<w:spacing w:line="360" w:lineRule="auto" w:after="120"/>',
    ]
    if indent:
        ppr.append('<w:ind w:firstLine="709"/>')
    if page_break_before:
        ppr.append("<w:pageBreakBefore/>")
    return f"<w:p><w:pPr>{''.join(ppr)}</w:pPr>{w_run(text, bold=bold, italic=italic, size=size)}</w:p>"


def w_heading(text: str, page_break: bool = True) -> str:
    return w_paragraph(
        text,
        align="center",
        indent=False,
        bold=True,
        size=32,
        page_break_before=page_break,
    )


def w_page_break() -> str:
    return '<w:p><w:r><w:br w:type="page"/></w:r></w:p>'


def w_table(rows: list[list[str]]) -> str:
    out = ['<w:tbl><w:tblPr><w:tblW w:w="0" w:type="auto"/><w:tblBorders><w:top w:val="single" w:sz="4"/><w:left w:val="single" w:sz="4"/><w:bottom w:val="single" w:sz="4"/><w:right w:val="single" w:sz="4"/><w:insideH w:val="single" w:sz="4"/><w:insideV w:val="single" w:sz="4"/></w:tblBorders></w:tblPr>']
    for row in rows:
        out.append("<w:tr>")
        for cell in row:
            out.append(
                '<w:tc><w:tcPr><w:tcW w:w="2400" w:type="dxa"/></w:tcPr>'
                + w_paragraph(cell, indent=False, size=24)
                + "</w:tc>"
            )
        out.append("</w:tr>")
    out.append("</w:tbl>")
    return "".join(out)


def table_from_html(block: str) -> str:
    caption_match = re.search(r"<caption>(.*?)</caption>", block, flags=re.S)
    pieces: list[str] = []
    if caption_match:
        pieces.append(w_paragraph(strip_tags(caption_match.group(1)), indent=False, italic=True, size=24))
    rows: list[list[str]] = []
    for tr in re.findall(r"<tr>(.*?)</tr>", block, flags=re.S):
        cells = re.findall(r"<t[dh]>(.*?)</t[dh]>", tr, flags=re.S)
        rows.append([strip_tags(c) for c in cells])
    if rows:
        pieces.append(w_table(rows))
    return "".join(pieces)


def block_to_word(block: str) -> str:
    block = block.strip()
    if block.startswith("<table"):
        return table_from_html(block)
    if block.startswith("<div"):
        return w_paragraph(strip_tags(block), align="center", indent=False, italic=True)
    if block.startswith("<ol"):
        items = re.findall(r"<li>(.*?)</li>", block, flags=re.S)
        return "".join(w_paragraph(f"{i}. {strip_tags(item)}", indent=False) for i, item in enumerate(items, 1))
    return w_paragraph(strip_tags(block) if block.startswith("<") else block)


