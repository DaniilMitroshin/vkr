def display_title(title: str) -> str:
    mapping = {
        "РЕФЕРАТ": "Реферат",
        "ABSTRACT": "Abstract",
        "ВВЕДЕНИЕ": "Введение",
        "СПИСОК ИСПОЛЬЗОВАННЫХ ИСТОЧНИКОВ": "Список использованной литературы",
        "ПРИЛОЖЕНИЯ": "Приложения",
    }
    if title.startswith("ГЛАВА "):
        num, _, rest = title.removeprefix("ГЛАВА ").partition(". ")
        return f"Глава {num}. {rest.capitalize()}"
    return mapping.get(title, title)


