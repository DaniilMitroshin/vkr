def w_petro_run(text: str, bold: bool = False, size: int = 28) -> str:
    props = [
        '<w:rFonts w:ascii="Times New Roman" w:eastAsia="Times New Roman" w:hAnsi="Times New Roman" w:cs="Times New Roman"/>',
        '<w:color w:val="000000"/>',
        f'<w:sz w:val="{size}"/>',
        f'<w:szCs w:val="{size}"/>',
    ]
    if bold:
        props.insert(1, "<w:b/>")
        props.insert(2, "<w:bCs/>")
    return f"<w:r><w:rPr>{''.join(props)}</w:rPr><w:t xml:space=\"preserve\">{xml_escape(text)}</w:t></w:r>"


def w_petro_paragraph(
    text: str,
    *,
    style: str | None = None,
    align: str | None = None,
    indent_first: bool = False,
    left: int | None = None,
    right: int | None = None,
    after: int | None = None,
    line: int | None = None,
    bold: bool = False,
    size: int = 28,
    page_break_before: bool = False,
) -> str:
    ppr: list[str] = []
    if style:
        ppr.append(f'<w:pStyle w:val="{style}"/>')
    if page_break_before:
        ppr.append("<w:pageBreakBefore/>")
    if after is not None or line is not None:
        attrs = []
        if after is not None:
            attrs.append(f'w:after="{after}"')
        if line is not None:
            attrs.append(f'w:line="{line}" w:lineRule="auto"')
        ppr.append(f"<w:spacing {' '.join(attrs)}/>")
    ind_attrs = []
    if left is not None:
        ind_attrs.append(f'w:left="{left}"')
    if right is not None:
        ind_attrs.append(f'w:right="{right}"')
    if indent_first:
        ind_attrs.append('w:firstLine="709"')
    if ind_attrs:
        ppr.append(f"<w:ind {' '.join(ind_attrs)}/>")
    if align:
        ppr.append(f'<w:jc w:val="{align}"/>')
    return f"<w:p><w:pPr>{''.join(ppr)}</w:pPr>{w_petro_run(text, bold=bold, size=size)}</w:p>"


def w_petro_break() -> str:
    return '<w:p><w:r><w:br w:type="page"/></w:r></w:p>'


def w_petro_heading(text: str, *, page_break: bool = True, level: int = 1) -> str:
    style = str(min(max(level, 1), 3))
    return w_petro_paragraph(text, style=style, page_break_before=page_break)


def w_petro_toc_line(text: str, page: str = "") -> str:
    return w_petro_paragraph(f"{text}{page}", style="11")


def w_petro_table(rows: list[list[str]]) -> str:
    out = ['<w:tbl><w:tblPr><w:tblW w:w="0" w:type="auto"/><w:tblBorders><w:top w:val="single" w:sz="4"/><w:left w:val="single" w:sz="4"/><w:bottom w:val="single" w:sz="4"/><w:right w:val="single" w:sz="4"/><w:insideH w:val="single" w:sz="4"/><w:insideV w:val="single" w:sz="4"/></w:tblBorders></w:tblPr>']
    for row in rows:
        out.append("<w:tr>")
        for cell in row:
            out.append(
                '<w:tc><w:tcPr><w:tcW w:w="2400" w:type="dxa"/></w:tcPr>'
                + w_petro_paragraph(cell, align="left", size=24, after=60, line=259)
                + "</w:tc>"
            )
        out.append("</w:tr>")
    out.append("</w:tbl>")
    return "".join(out)


def petro_table_from_html(block: str) -> str:
    caption_match = re.search(r"<caption>(.*?)</caption>", block, flags=re.S)
    pieces: list[str] = []
    if caption_match:
        pieces.append(w_petro_paragraph(strip_tags(caption_match.group(1)), align="left", size=24, after=80, line=259))
    rows: list[list[str]] = []
    for tr in re.findall(r"<tr>(.*?)</tr>", block, flags=re.S):
        cells = re.findall(r"<t[dh]>(.*?)</t[dh]>", tr, flags=re.S)
        rows.append([strip_tags(c) for c in cells])
    if rows:
        pieces.append(w_petro_table(rows))
    return "".join(pieces)


def block_to_petro_word(block: str) -> str:
    block = block.strip()
    if block.startswith("<table"):
        return petro_table_from_html(block)
    if block.startswith("<div"):
        return w_petro_paragraph(strip_tags(block), align="center", size=24, after=180, line=259)
    if block.startswith("<ol"):
        items = re.findall(r"<li>(.*?)</li>", block, flags=re.S)
        return "".join(w_petro_paragraph(f"{i}. {strip_tags(item)}", indent_first=False, after=80, line=259) for i, item in enumerate(items, 1))
    return w_petro_paragraph(strip_tags(block) if block.startswith("<") else block, style="a", indent_first=True)


